package firstcode;

public class Switch {
	public static void main(String[] args) {
		
		int value=1000;
		
		switch(value) {
		default:
			System.out.println("no salary bro");
			break;
		case 1000:
			System.out.println("you got a good salary");
			break;
		case 500:
			System.out.println("you got a bad salary");
			break;
			
		}
	}
}
